ITSUSER.ITSID as ITSUSERITSID, ITSUSER.IDBIRTH as ITSUSERIDBIRTH, ITSUSER.ITSPASSWORD as ITSUSERITSPASSWORD, 
ITSUSER.IDDATABASEBIRTH as ITSUSERIDDATABASEBIRTH, ITSROLE.ITSNAME as ITSROLEITSNAME, 
